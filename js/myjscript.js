$(window).scroll(function(){
    if(($(window).scrollTop()>100)&&($(window).scrollTop()<($(document).height()-$(window).height()-110))){
        $("#TopButton").css("visibility", "visible");
        $("#TopButton").stop().animate({opacity:0.7},200);
    } else {
        $("#TopButton").stop().animate({opacity:0},200, "linear", function(){
        $("#TopButton").css("visibility", "hidden");
        });
    }
});
